	    <dt class="blacklight-summary_display">Description:</dt>
	    <dd class="blacklight-summary_display">MS 41 contains the Old English translation of Bede's <i>Historia ecclesiastica</i>. The Old English translation, commonly referred to as the <i>Old English Bede</i>  was written into MS 41 during the first half of the eleventh century probably somewhere in the south of England. The wide margins of MS 41 contain a variety of marginalia, ranging from the Old English poem <i>Solomon and Saturn</i> to Latin liturgical texts as well as Old English and Latin charms. The manuscript contains two interesting inscriptions. On pages 483-4 the unnamed scribe of the manuscript wrote a prayer seeking blessings from its readers and on page 488 a bilingual inscription states that MS 41 was one of the manuscripts given to Exeter Cathedral by Bishop Leofric (1050-72). The manuscript's decoration consists of numerous ornated initials in brown outline and also features two remarkable line drawings; one of Christ crucified on page 484 and another figure on page 485.</dd>
